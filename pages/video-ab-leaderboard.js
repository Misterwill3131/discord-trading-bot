+ '      + "<span class=\\"row-date\\">" + dateStr + "</span>"'
+ '    + "</div>"'
+ '    + "<div class=\\"variants\\">"'
+ '      + "<div class=\\"variant" + (winA ? " winner-cell" : "") + "\\">"'
+ '        + "<div class=\\"variant-label\\">Variant A · Job #" + p.jobA.id + "</div>"'
+ '        + "<div class=\\"variant-template\\">" + tplA + "</div>"'
+ '        + "<div class=\\"variant-reactions\\"><span class=\\"n\\">" + rA + "</span><span class=\\"lbl\\">reactions</span></div>"'
+ '      + "</div>"'
+ '      + "<div class=\\"variant" + (winB ? " winner-cell" : "") + "\\">"'
+ '        + "<div class=\\"variant-label\\">Variant B · Job #" + p.jobB.id + "</div>"'
+ '        + "<div class=\\"variant-template\\">" + tplB + "</div>"'
+ '        + "<div class=\\"variant-reactions\\"><span class=\\"n\\">" + rB + "</span><span class=\\"lbl\\">reactions</span></div>"'
+ '      + "</div>"'
+ '    + "</div>"'
+ '    + "<div class=\\"delta-bar\\"><div class=\\"part-A\\" style=\\"width:" + pctA.toFixed(1) + "%\\"></div><div class=\\"part-B\\" style=\\"width:" + pctB.toFixed(1) + "%\\"></div></div>"'
+ '  + "</div>";'
+ '}'
+ 'function winnerLabel(p) {'
+ '  if (p.winner === "A") return "🏆 A wins +" + p.delta;'
+ '  if (p.winner === "B") return "🏆 B wins +" + p.delta;'
+ '  if (p.winner === "tie") return "🤝 Tie";'
+ '  return "⏳ Unknown";'
+ '}'
+ 'load();'
+ '</script>'
+ '</body></html>';

