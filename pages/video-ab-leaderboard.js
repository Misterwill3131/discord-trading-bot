+ '.variant-reactions .n { font-size: 28px; font-weight: 900; color: #fafafa; }'
+ '.variant-reactions .lbl { font-size: 11px; color: #80848e; }'
+ '.delta-bar { display: flex; height: 8px; border-radius: 4px; overflow: hidden; margin-top: 12px; background: rgba(255,255,255,0.04); }'
+ '.delta-bar .part-A { background: linear-gradient(90deg, #10b981, #059669); }'
+ '.delta-bar .part-B { background: linear-gradient(90deg, #3b82f6, #1e40af); }'
+ '.empty { color: #6b7280; text-align: center; padding: 60px 0; font-size: 14px; }'
