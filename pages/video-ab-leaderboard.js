const VIDEO_AB_LEADERBOARD_HTML = '<!DOCTYPE html><html lang="fr"><head><meta charset="UTF-8">'
+ '<title>A/B Results — BOOM</title>'
+ '<style>'
+ COMMON_CSS
+ '#wrap { padding: 24px; max-width: 1100px; margin: 0 auto; }'
+ 'h1 { font-size: 22px; font-weight: 700; margin-bottom: 6px; }'
