+ '.winner-unknown { background: rgba(255,255,255,0.06); color: #80848e; }'
+ '.variants { display: grid; grid-template-columns: 1fr 1fr; gap: 10px; }'
+ '.variant { background: rgba(255,255,255,0.02); border: 1px solid rgba(255,255,255,0.06); border-radius: 8px; padding: 12px; transition: border-color .2s; }'
+ '.variant.winner-cell { border-color: #10b981; box-shadow: 0 0 0 1px rgba(16,185,129,0.3) inset; }'
